'use client'

import { type SocialLinks } from '@/content/eazie-content'

interface SocialIconRowProps {
  links: SocialLinks
  large?: boolean
}

export default function SocialIconRow({ links, large = false }: SocialIconRowProps) {
  const size = large ? 22 : 18
  const btnSize = large ? 'w-12 h-12' : 'w-10 h-10'

  const items = [
    { key: 'instagram', href: links.instagram, label: 'Instagram', icon: <InstagramIcon size={size} /> },
    { key: 'spotify', href: links.spotify, label: 'Spotify', icon: <SpotifyIcon size={size} /> },
    { key: 'youtube', href: links.youtube, label: 'YouTube', icon: <YouTubeIcon size={size} /> },
    { key: 'tiktok', href: links.tiktok, label: 'TikTok', icon: <TikTokIcon size={size} /> },
    { key: 'x', href: links.x, label: 'X', icon: <XIcon size={size} /> },
    { key: 'twitch', href: links.twitch, label: 'Twitch', icon: <TwitchIcon size={size} /> },
  ].filter((item) => item.href)

  if (items.length === 0) return null

  return (
    <div className="flex items-center justify-center flex-wrap gap-3">
      {items.map((item) => (
        <a
          key={item.key}
          href={item.href}
          target="_blank"
          rel="noopener noreferrer"
          aria-label={item.label}
          className={`${btnSize} rounded-full flex items-center justify-center text-text-secondary hover:text-white transition-all duration-300 hover:scale-110 active:scale-95`}
          style={{
            background: 'rgba(255,255,255,0.05)',
            border: '1px solid rgba(255,255,255,0.08)',
          }}
        >
          {item.icon}
        </a>
      ))}
    </div>
  )
}

